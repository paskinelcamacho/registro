<h2> Bienvenida </h2>
<br/>
<p> Misión<br/>

Proporcionar la infraestructura y servicios de Tecnologías de Información y Comunicaciones que requiere la Facultad para su buen funcionamiento en el desarrollo de las actividades académico-administrativas de la misma. 
<br/>
Visión
<br/>
Ser un área de servicios que brinde de manera eficiente y oportuna soluciones de Tecnologías de Información y Comunicaciones que impulsen y favorezcan de manera continua el desarrollo e innovación tecnológica de la Facultad. 
<br/>

Valores
<br/>
El Centro de Informática ofrece todos los días sus servicios en el marco de los siguientes valores:
<ul>
    <li>Responsabilidad</li>
    <li>Integridad y ética</li>
    <li>Respeto</li>
    <li>Actitud de servicio</li>
    <li>Trabajo en equipo</li>
    <li>Mejora continua</li>
    <li>Compromiso social</li>
</ul>
